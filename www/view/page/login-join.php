function selectAllClick(obj) {
	if ($(obj).prop('checked') == true) {
		$(obj).prop('checked',true);
		$(".login__check__option").find('.select').prop('checked',true);
	} else {
		$(obj).attr('checked',false);
		$(".login__check__option").find('.select').prop('checked',false);
	}
