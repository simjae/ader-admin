function passwordConfirm(str){    
    //  대소문자/숫자/특수문자 중 3가지 이상 조합, 8자-16자
    //  입력 가능 특수문자 : '!@#$%^()_-={}[]|;:<>,.?/                    
    var password_reg = /^(?=.*[\{\}\[\]\/?.,;:|\)*`!^\-_<>@\#$%\=\(])(?=.*\d)(?=.*[a-z])(?=.*[A-Z])[\da-zA-Z\{\}\[\]\/?.,;:|\)*`!^\-_<>@\#$%\=\(]{8,16}/;
    //  공백 입력 불가능
    var space_reg = /\s/g;
    //var password_str = $('input[name="password"]').eq(0).val();

    if(space_reg.test(str) == false){
        if(password_reg.test(str)){
            return true;
        }
        else{
            return false;
        }
    }
    else{
        return false;
        //공백 포함 예외처리
    }
}
